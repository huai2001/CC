-(WKUserScript*)inject:(NSString *)path {
    NSData *reader = [NSData dataWithContentsOfFile:path];
    NSString *sString = [[NSString alloc] initWithData:reader encoding:NSUTF8StringEncoding];
    //用于进行JavaScript注入
    return [[WKUserScript alloc] initWithSource:sString injectionTime:WKUserScriptInjectionTimeAtDocumentEnd forMainFrameOnly:YES];
}
