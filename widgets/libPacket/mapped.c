/**/
byte_t _cc_encrypt_mapping(byte_t* encrypt,
                           const byte_t* source,
                           int32_t source_size) {
    int32_t i = 0;
    uint8_t crc = 0;
    uint8_t* table = _cc_build_crc8_msb_table(0xAB);

    _cc_assert(source != NULL);
    if (encrypt == NULL) {
        encrypt = (byte_t*)source;
    }

    for (; i < source_size; i++) {
        crc = table[(crc ^ source[i]) & 0xFF];
        encrypt[i] = _encryptMappingTable[source[i]];
    }

    return crc;
}

/**/
byte_t _cc_decrypt_mapping(byte_t* source,
                           const byte_t* encrypt,
                           int32_t encrypt_size) {
    int32_t i = 0;
    uint8_t crc = 0;
    uint8_t* table = _cc_build_crc8_msb_table(0xAB);

    _cc_assert(encrypt != NULL);
    if (source == NULL) {
        source = (byte_t*)encrypt;
    }

    for (; i < encrypt_size; i++) {
        source[i] = _decryptMappingTable[encrypt[i]];
        crc = table[(crc ^ source[i]) & 0xFF];
    }

    return crc;
}
